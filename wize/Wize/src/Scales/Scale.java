package Scales;

public class Scale {
    private IScale _scale = null;

    public Scale(IScale scale) {
        _scale = scale;
    }

    public boolean Connect() {
        return _scale.Connect();
    }

    public void Run(Boolean isRunning) {
        _scale.Run(isRunning);
    }

    public void Disconnect() {
        _scale.Disconnect();
    }

    public String Data() {
        return _scale.Data();
    }

    public boolean IsDataRecieved() {
        return _scale.IsDataRecieved();
    }

    public void ClearData() {
        _scale.ClearData();
    }
}